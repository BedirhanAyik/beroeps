<div class="Text1" id="Fortica">
    <h1>Bezoek het Fortica Starigrad fort</h1><br>
De inwoners van Omis zijn altijd al goede zeelieden geweest, vooral tussen de 13e en 15e eeuw,<br>
toen ze de macht hadden over de scheepvaart in de Adriatische Zee.<br>
Om een zo goed mogelijk panoramisch uitzicht te hebben over de zee, werd een zeer hoog fort gebouwd.<br>
Het Fortica Starigrad is een fort op een heuvel boven de stad, dat speciaal voor deze strategische reden werd gebouwd.<br>
Het was een uitkijkpunt van waaruit piraten konden worden gesignaleerd en een eventuele aanval op de stad kon worden voorkomen.<br>
De inwoners van Omis waren zo goed voorbereid, dat ze zelfs grote rotsen klaar hadden liggen om vanaf het fort naar beneden te gooien wanneer iemand de stad aan wilde vallen.<br>
Gelukkig hebben ze deze rotsen nooit echt hoeven gebruiken.<br>
<br>
"Om Fortica Starigrad te bereiken vanuit het centrum volg je het gemarkeerde pad tot bovenaan de heuvel. Het is een (vrij intensieve) wandeling van ongeveer 25 minuten".<br>
