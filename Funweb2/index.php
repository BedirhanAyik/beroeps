    <br>
    <h1>Zipline</h1><br>
    Zip Line-tour laat de staaldraad zakken op een hoogte van 150 meter boven de prachtige rivier Cetina-kloof.<br>
    Het bestaat uit 8 draden met een totale lengte van 2100 m (de langste draad 700 m) en het is mogelijk om snelheden van 65 km / u te bereiken.<br>
    De reis vindt plaats op 3 km van Omis in de kloof van de rivier de Cetina en bestaat uit een korte wandeling door de natuur en training voordat je over de draad klimt.<br>
    Het wordt aanbevolen om geschikt schoeisel te dragen om in de natuur te wandelen en water (1 liter) mee te nemen. Minimum leeftijd is 7 jaar., Het maximum is onbeperkt.<br>
    Het maximale gewicht is 140 kg voor een gast.<br>
