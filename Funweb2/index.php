    <h1>Verken het oude centrum</h1><br>
    Vergeleken met andere kuststeden is Omis niet zo groot, maar er valt genoeg te zien.<br>
    Je kunt de belangrijkste kerk bezoeken, de kerk van St. Michael, uit de 17e eeuw.<br>
    Deze kerk heeft het wapen van Omis op haar muren staan. In het stadsmuseum van Omis kun je zien hoe de inwoners van het binnenland van Omis (Poljica) vroeger leefden.<br>
    Je vindt er ook sieraden en munten uit de Middeleeuwen die zeer goed bewaard zijn gebleven.<br>

    Wat de meeste bezoekers het leukste vinden aan Omis is om te wandelen door de smalle straatjes vol souvenirwinkeltjes en restaurants.<br>
    Je kunt er heerlijke traditionele Kroatische gerechten proeven, zoals zwarte risotto met inktvis of Dalmatische pasticada.<br>
