    <h1> Zomerevenementen </h1> <br>
    Omis is in het zomerseizoen bijna net zo levendig als het vlakbij gelegen Split. Er worden veel festivals<br>
    gehouden, evenals shows en concerten, die zowel bij de lokale bevolking als bij de toeristen populair zijn.<br>
<br>
    De Piratenstrijd van Omis wordt ieder jaar in augustus gehouden. Het is een traditioneel evenement<br>
    dat in het teken staat van de beroemde strijd tegen piraten die in de 13e eeuw bij Omis heeft <br>
    De reconstructie van de strijd is zeer gedetailleerd en wordt uitgevoerd door<br>
